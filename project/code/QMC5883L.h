#ifndef _ZF_DRIVER_QMC5883L_H_
#define _ZF_DRIVER_QMC5883L_H_

#include "zf_common_typedef.h"

// ====================================================配置====================================================
// 接线定义
// 模块管脚   单片机管脚
// SCL       查看下方 QMC5883L_SCL_PIN 宏定义
// SDA       查看下方 QMC5883L_SDA_PIN 宏定义
// VCC       3.3V电源
// GND       电源地
// DRDY      不使用（悬空）

// 引脚定义
#define QMC5883L_SCL_PIN           P14_4    // SCL 引脚
#define QMC5883L_SDA_PIN           P12_5    // SDA 引脚
#define QMC5883L_SOFT_IIC_DELAY    (59)     // 软件 IIC 时钟延时周期

// 芯片地址
#define QMC5883L_DEV_ADDR          (0x0D)   // 7位I2C地址

// 寄存器地址
#define QMC5883L_REG_DATA_X_LSB    0x00
#define QMC5883L_REG_DATA_X_MSB    0x01
#define QMC5883L_REG_DATA_Y_LSB    0x02
#define QMC5883L_REG_DATA_Y_MSB    0x03
#define QMC5883L_REG_DATA_Z_LSB    0x04
#define QMC5883L_REG_DATA_Z_MSB    0x05
#define QMC5883L_REG_STATUS        0x06
#define QMC5883L_REG_TEMP_LSB      0x07
#define QMC5883L_REG_TEMP_MSB      0x08
#define QMC5883L_REG_CONTROL_1     0x09
#define QMC5883L_REG_CONTROL_2     0x0A
#define QMC5883L_REG_SET_RESET     0x0B
#define QMC5883L_REG_CHIP_ID       0x0D

// 控制寄存器1配置
// 寄存器位布局: Bit7-6: OSR[1:0], Bit5-4: RNG[1:0], Bit3-2: ODR[1:0], Bit1-0: MODE[1:0]
#define QMC5883L_MODE_STANDBY      (0x00)  // 待机模式
#define QMC5883L_MODE_CONTINUOUS   (0x01)  // 连续测量模式
#define QMC5883L_ODR_10HZ          (0x00)  // 10Hz
#define QMC5883L_ODR_50HZ          (0x04)  // 50Hz
#define QMC5883L_ODR_100HZ         (0x08)  // 100Hz
#define QMC5883L_ODR_200HZ         (0x0C)  // 200Hz
#define QMC5883L_RNG_2G            (0x00)  // ±2G
#define QMC5883L_RNG_8G            (0x10)  // ±8G
#define QMC5883L_OSR_512           (0x00)  // 512次过采样
#define QMC5883L_OSR_256           (0x40)  // 256次过采样
#define QMC5883L_OSR_128           (0x80)  // 128次过采样
#define QMC5883L_OSR_64            (0xC0)  // 64次过采样

// 默认配置
#define QMC5883L_DEFAULT_CONFIG    (QMC5883L_MODE_CONTINUOUS | \
                                    QMC5883L_ODR_100HZ | \
                                    QMC5883L_RNG_2G | \
                                    QMC5883L_OSR_512)

// 状态寄存器位定义
#define QMC5883L_STATUS_DRDY       (0x01)  // 数据就绪
#define QMC5883L_STATUS_OVL        (0x02)  // 数据溢出
#define QMC5883L_STATUS_DOR        (0x04)  // 数据跳过

// 芯片ID
#define QMC5883L_CHIP_ID           (0xFF)  // 芯片ID寄存器固定返回值

// 灵敏度参数
#define QMC5883L_SENSITIVITY_2G    (12000.0f)  // ±2G量程灵敏度: 12000 LSB/高斯
#define QMC5883L_SENSITIVITY_8G    (3000.0f)   // ±8G量程灵敏度: 3000 LSB/高斯
#define QMC5883L_TEMP_COEFF        (100.0f)    // 温度传感器系数: 100 LSB/℃

// 超时计数
#define QMC5883L_TIMEOUT_COUNT     (0x00FF)    // 超时计数

// ====================================================全局变量声明====================================================
// 声明为外部变量，可在其他文件中访问
extern int16 qmc5883l_mag_x;          // X轴磁场强度原始值
extern int16 qmc5883l_mag_y;          // Y轴磁场强度原始值
extern int16 qmc5883l_mag_z;          // Z轴磁场强度原始值
extern float qmc5883l_mag_x_gauss;    // X轴磁场强度（高斯）
extern float qmc5883l_mag_y_gauss;    // Y轴磁场强度（高斯）
extern float qmc5883l_mag_z_gauss;    // Z轴磁场强度（高斯）
extern float qmc5883l_temperature;    // 温度值（℃）
extern float qmc5883l_heading;        // 航向角（度，0-360）

// ====================================================函数声明====================================================
//-------------------------------------------------------------------------------------------------------------------
// 函数简介     获取 QMC5883L 磁场数据
// 参数说明     void
// 返回参数     void
// 使用示例     qmc5883l_get_mag();
// 备注信息     执行该函数后，直接查看对应的全局变量即可
//-------------------------------------------------------------------------------------------------------------------
void qmc5883l_get_mag(void);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     获取 QMC5883L 温度数据
// 参数说明     void
// 返回参数     void
// 使用示例     qmc5883l_get_temp();
// 备注信息     执行该函数后，直接查看对应的全局变量即可
//-------------------------------------------------------------------------------------------------------------------
void qmc5883l_get_temp(void);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     获取 QMC5883L 所有数据
// 参数说明     void
// 返回参数     void
// 使用示例     qmc5883l_get_all();
// 备注信息     获取磁场、温度和航向角数据
//-------------------------------------------------------------------------------------------------------------------
void qmc5883l_get_all(void);//10ms

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     将 QMC5883L 磁场数据转换为实际物理数据
// 参数说明     mag_value    任意轴的磁场强度原始值
// 返回参数     float        转换后的磁场强度（高斯）
// 使用示例     float data = qmc5883l_mag_transition(qmc5883l_mag_x);
// 备注信息     根据当前量程自动选择转换系数
//-------------------------------------------------------------------------------------------------------------------
float qmc5883l_mag_transition(int16 mag_value);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     计算航向角
// 参数说明     void
// 返回参数     float        航向角（度，0-360）
// 使用示例     float heading = qmc5883l_calculate_heading();
// 备注信息     基于当前磁场数据计算航向角
//-------------------------------------------------------------------------------------------------------------------
float qmc5883l_calculate_heading(void);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     初始化 QMC5883L
// 参数说明     void
// 返回参数     uint8        1-初始化失败 0-初始化成功
// 使用示例     qmc5883l_init();
// 备注信息
//-------------------------------------------------------------------------------------------------------------------
uint8 qmc5883l_init(void);

#endif